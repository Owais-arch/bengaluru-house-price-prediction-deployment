import streamlit as st
import pickle
import numpy as np

# Load trained model
model = pickle.load(open("bangalore_house_model.pkl", "rb"))

# Title of app
st.title("Bengaluru House Price Prediction")

st.write("Enter the house details to predict the price")

# User inputs
sqft = st.number_input("Total Square Feet", min_value=300)

bath = st.number_input("Number of Bathrooms", min_value=1)

bhk = st.number_input("Number of BHK", min_value=1)

# Prediction button
if st.button("Predict Price"):

    input_data = np.array([[sqft, bath, bhk]])

    prediction = model.predict(input_data)

    st.success(f"Estimated House Price: ₹ {prediction[0]:.2f} Lakhs")