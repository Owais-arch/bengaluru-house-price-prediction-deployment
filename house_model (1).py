import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df=pd.read_csv("/content/Bengaluru_House_Data.csv")

df.head()

df.shape

df.info()

df.isnull().sum()[df.isnull().sum()>0]

df = df.dropna(subset=['location','size'])

df['society'] = df['society'].fillna('Unknown')

df['bath'] = df['bath'].fillna(df['bath'].median())

df['balcony'] = df['balcony'].fillna(df['balcony'].median())

df.isnull().sum()

df.shape

df['bhk'] = df['size'].apply(lambda x: int(x.split(' ')[0]))

df['total_sqft']=pd.to_numeric(df['total_sqft'],errors='coerce')

df.isnull().sum()

df['total_sqft']=df['total_sqft'].fillna(df['total_sqft'].mean())

df['price_per_sqft'] = (df['price']*100000)/df['total_sqft']

df = df[(df.total_sqft/df.bhk)>=300]

df_train_x = df.drop(['price','price_per_sqft'], axis=1)

df.info()

df.location.nunique()

df.location = df.location.apply(lambda x: x.strip())

location_count = df.location.value_counts()

locations_less_10 = location_count[location_count<=10]

df.location = df.location.apply(lambda x: 'other' if x in locations_less_10 else x)

df = pd.get_dummies(df, columns=['location','area_type','availability','society'],dtype=int)

df=df.drop(['size'],axis=1)

df.info()

df



from sklearn.model_selection import train_test_split
df_train,df_test=train_test_split(df,test_size=0.2)

df_train_x=df_train.drop(['price'],axis=1)
df_train_y=df_train['price']

df_test_x=df_test.drop(['price'],axis=1)
df_test_y=df_test['price']

import json

columns = {
    "data_columns": df_train_x.columns.tolist()
}

with open("columns.json", "w") as f:
    json.dump(columns, f)

print("columns.json created successfully")

from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score # Corrected import for r2_score
model=LinearRegression()
model.fit(df_train_x,df_train_y)

y_pred = model.predict(df_test_x)

r2_score(df_test_y,y_pred)

from sklearn.ensemble import RandomForestRegressor

rf = RandomForestRegressor()

rf.fit(df_train_x, df_train_y)

pred=rf.predict(df_test_x)

r2_score(df_test_y,pred)

import pickle

pickle.dump(rf,open("bangalore_house_model.pkl","wb"))

